
public class Input2 {
	private String cpid;
	private Integer type;
	public Input2(String cpid, Integer type) {
		this.cpid = cpid;
		this.type = type;
	}
	public String getCpid() {
		return cpid;
	}
	public void setCpid(String cpid) {
		this.cpid = cpid;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	
	

}
